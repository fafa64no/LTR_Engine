#pragma once

#include "LTR_Engine_lib.h"

// ############################################################################
//                            Material Structs
// ############################################################################
struct Material {
    glm::vec3 ambient;
    glm::vec3 diffuse;
    glm::vec3 specular;
    int shininess;
}; 

// ############################################################################
//                            Material Globals
// ############################################################################
static Material metal;

// ############################################################################
//                            Material Functions
// ############################################################################
void gl_materials_init(){
    metal.ambient=glm::vec3(0.25,0.25,0.25);
    metal.diffuse=glm::vec3(0.4,0.4,0.4);
    metal.specular=glm::vec3(0.774597,0.774597,0.774597);
    metal.shininess=77;
}